<template>
  <div>
    <el-card class="box-card">
      <h2>Login</h2>
      <el-form
        :model="ruleForm"
        status-icon
        :rules="rules"
        ref="ruleForm"
        label-position="left"
        label-width="80px"
        class="login-from"
      >
        <el-form-item label="Username" prop="uname">
          <el-input v-model="ruleForm.uname"></el-input>
        </el-form-item>
        <el-form-item label="Password" prop="password">
          <el-input
            type="password"
            v-model="ruleForm.password"
            autocomplete="off"
          ></el-input>
        </el-form-item>
      </el-form>
      <div class="btnGroup">
        <el-button type="primary" @click="submitForm('ruleForm')"
          >Login</el-button
        >
        <el-button @click="resetForm('ruleForm')">Reset</el-button>
        <router-link to="/SignUp.vue">
          <el-button style="margin-left: 10px">Sign Up</el-button>
        </router-link>
      </div>
    </el-card>
  </div>
</template>

<script>
export default {
  data() {
    return {
      ruleForm: {
        uname: '',
        password: ''
      },
      rules: {
        uname: [
          {
            required: true,
            message: 'Username can not be empty!',
            trigger: 'blur'
          }
        ],
        password: [
          {
            required: true,
            message: 'Password can not be empty!',
            trigger: 'blur'
          }
        ]
      }
    }
  },
  methods: {
    submitForm(formName) {
      this.$refs[formName].validate((valid) => {
        if (valid) {
          alert('submit!')
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    resetForm(formName) {
      this.$refs[formName].resetFields()
    }
  }
}
</script>

